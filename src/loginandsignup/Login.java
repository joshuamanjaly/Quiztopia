    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {                                         
    String email = jTextField1.getText();
    String password = new String(jPasswordField1.getPassword());

    if (email.isEmpty() || password.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please fill in all fields", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    try {
        // Establish a MySQL connection
        Connection connection = DriverManager.getConnection("jdbc:mysql://localhost:3306/quiztopia", "root", "");

        // Create a SQL query
        String query = "SELECT * FROM users WHERE email = ? AND password = ?";
        PreparedStatement preparedStatement = connection.prepareStatement(query);
        preparedStatement.setString(1, email);
        preparedStatement.setString(2, password);

        // Execute the SQL query
        ResultSet resultSet = preparedStatement.executeQuery();

        if (resultSet.next()) {
            // Successful login
            JOptionPane.showMessageDialog(this, "Login Successful", "Success", JOptionPane.INFORMATION_MESSAGE);

            // Open StudentHomepage
            StudentHomepage studentHomepage = new StudentHomepage();
            studentHomepage.setVisible(true);
            studentHomepage.pack();
            studentHomepage.setLocationRelativeTo(null);

            // Close the login window
            this.dispose();
        } else {
            // Failed login
            JOptionPane.showMessageDialog(this, "Invalid email or password", "Error", JOptionPane.ERROR_MESSAGE);
        }

        // Close resources
        resultSet.close();
        preparedStatement.close();
        connection.close();

    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(this, "Database error: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
}


